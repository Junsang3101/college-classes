i = 1
f = 1.0
c = 'a'

print("Python에서 format specifiers 와 같이 %를 출력 하라면 %%를 사용")
print("%d 실수형 format specifier %%f " % f)
print("%d 10진수형 format specifier %%d" % i)
print("%o 8진수형 format specifier %%f" % i)
print("%x 16진수형 format specifier %%f" % i)
print("%c 문자형 format specifier %%c" % c)
print("%s 문자열형 format specifier %%s" % c)