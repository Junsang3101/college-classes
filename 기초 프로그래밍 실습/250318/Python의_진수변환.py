print("Python에서는 진수끼리 변환할 수 있다")
print('2진수(binary number)로 바꾸는 건 bin(var)')
print('8진수(octal number)로 바꾸는 건 oct(var)')
print('10진수(integer number)로 바꾸는 건 int(var)')
print('16진수(hexadecimal number)로 바꾸는 건 hex(var)')
print("integer는 4byte(32bit)로 2진수로 표현하면 0000 0000 0000 0000인데 이를 16진소로 간단하게 표현하면 0x0이다")
print("1111 1111 1111 1111은 0xFFFF으로 1byte(1111)을 16진수로 바꾼 것이다. 이렇게 압축하면 앞에 '0x'를 붙이는데")
print("이것은 16진수라고 명시하는 것이고 8진수로 바꾼 경우에는 '0' 2진수는 '0b'를 붙인다")
print("이렇게 변환하는 것을 '진수변환'이라고 한다")